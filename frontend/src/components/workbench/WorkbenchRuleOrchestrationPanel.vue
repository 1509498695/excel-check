const ruleForm = reactive<{
  rule_id: string
  group_id: string
  rule_name: string
  target_variable_tag: string
  selected_rule: FixedRuleSelection
  expected_value: string
  reference_variable_tag: string
}>({
  rule_id: '',
  group_id: 'ungrouped',
  rule_name: '',
  target_variable_tag: '',
  selected_rule: 'gt',
  expected_value: '0',
  reference_variable_tag: '',
})

const compositeRuleForm = reactive<CompositeRuleConfig>({
  global_filters: [],
  branches: [],
})

// 规则组弹窗（替代原 ElMessageBox.prompt，与设计稿 DIALOG 04 对齐）
const isGroupDialogVisible = ref(false)
const groupDialogMode = ref<'create' | 'rename'>('create')
const groupForm = reactive<{ id: string; name: string }>({ id: '', name: '' })
const isSubmittingGroup = ref(false)

const ruleSelectionOptions: Array<{ label: string; value: FixedRuleSelection }> = [
  { label: '等于 (=)', value: 'eq' },
  { label: '不等于 (!=)', value: 'ne' },
  { label: '大于 (>)', value: 'gt' },
  { label: '小于 (<)', value: 'lt' },
  { label: '非空校验', value: 'not_null' },
  { label: '唯一校验', value: 'unique' },
  { label: '包含 (in)', value: 'in' },
]

const compositeFilterOptions: Array<{ label: string; value: CompositeFilterOperator }> = [
  { label: '等于 (=)', value: 'eq' },
  { label: '不等于 (!=)', value: 'ne' },
  { label: '大于 (>)', value: 'gt' },
  { label: '小于 (<)', value: 'lt' },
  { label: '非空校验', value: 'not_null' },
]

const compositeAssertionOptions: Array<{ label: string; value: CompositeAssertionOperator }> = [
  { label: '等于 (=)', value: 'eq' },
  { label: '不等于 (!=)', value: 'ne' },
  { label: '大于 (>)', value: 'gt' },
  { label: '小于 (<)', value: 'lt' },
  { label: '非空校验', value: 'not_null' },
  { label: '唯一校验', value: 'unique' },
  { label: '必须重复', value: 'duplicate_required' },
]

