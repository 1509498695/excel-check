  return [
    {
      label: keyColumn ? `${keyColumn} (Key)` : 'Key(映射键)',
      value: KEY_FIELD,
    },
    ...(variable.columns ?? [])
      .map((column) => column.trim())
      .filter((column) => column && column !== keyColumn)
      .map((column) => ({ label: column, value: column })),
  ]
