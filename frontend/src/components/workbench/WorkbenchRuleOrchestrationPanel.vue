// 保留原有业务逻辑：规则编排面板的状态管理、保存、删除等行为完全沿用 store。
const store = useWorkbenchStore()
const isRuleDialogVisible = ref(false)
const ruleDialogMode = ref<'create' | 'edit'>('create')
const isInitializingRuleDialog = ref(false)
